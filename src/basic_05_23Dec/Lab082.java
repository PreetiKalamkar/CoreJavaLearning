package src.basic_05_23Dec;

public class Lab082 {
    public static void main(String[] args) {

        // Print the odd number in between the 1 to 50.
        //If Else
        for(int i=1; i<=50; i++){
            if(i%2==1){     // (i%2==1) OR !(i%2==0)
                System.out.println(i + " is Odd!");
            }
        }
        System.out.println("--End of Program!!--");

        // (i%2==1) OR !(i%2==0)

    }
}
