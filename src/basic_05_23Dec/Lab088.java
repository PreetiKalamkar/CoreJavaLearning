package src.basic_05_23Dec;

public class Lab088 {
    public static void main(String[] args) {

        // while and do while

        int i = 1; // A - Init
        while (i<=10){ // B - condition
            System.out.println(i); // D- code execute
            i++; // C- I/D
        }

        for (int j=11; j<=15; j++){
            System.out.println(j);
        }

    }
}

