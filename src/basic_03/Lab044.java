public class Lab044 {
        public static void main(String[] args){

           // Decrement operator

            int b = 10;
            System.out.println(--b + b++ + ++b);
            System.out.println(b);

           // 9 + 9 + 11 = 29
           // 11

            // A -> --b , Exp ->9 , b ->9
            // B -> b++ , Exp 9 , b ->  10
            // C , Exp -> 11 , b - 11


        }
}
